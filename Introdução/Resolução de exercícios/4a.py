pizzas = int(input("Informe quantas pizzas de 12 pedaços serão divididas:"))
qtdPessoas = int(input("Informe o número de pessoas que irão comer:"))
divisao = (pizzas*12)/qtdPessoas
print(f'Se {pizzas} pizzas de 12 pedaços são divididas entre {qtdPessoas} pessoas, cada uma recebe {divisao} pedaços.')